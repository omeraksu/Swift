import UIKit

// Karşılaştırma Operatorleri
var s1 = 20
var s2 = 30
var s3 = 50
var s4 = 40

    // == eşittir
print(s1 == s2)

    // != eşit değildir
print(s1 != s2)
    
    // > büyüktür
print(s1 > s2)

    // < küçüktür
print(s1 < s2)

    // >= büyük eşit
print(s1 >= s2)

    // <= küçük eşit
print(s1 <= s2)


// Mantıksal Operatörler
